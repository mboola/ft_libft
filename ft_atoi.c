int	ft_atoi(const char *str)
{
	int				num;
	int				n;

	while (*str == '\f' || *str == '\n' || *str == '\r' || 
		*str == '\t' || *str == '\v' || *str == ' ')
		str++;
	n = 1;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			n *= -1;
		str++;
	}
	while (*str == '0')
		str++;
	num = 0;
	while (*str >= '0' && *str <= '9')
	{
		num = num * 10 + (*str - '0');
		str++;
	}
	num *= n;
	return (num);
}
